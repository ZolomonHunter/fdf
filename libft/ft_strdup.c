#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		i;

	str = malloc((ft_strlen(s1) + 1) * sizeof(*str));
	if (str == 0)
		return (0);
	i = 0;
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
