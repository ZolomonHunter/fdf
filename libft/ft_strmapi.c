#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*res;

	if (s == 0)
		return (0);
	i = 0;
	res = malloc(sizeof(*res) * (ft_strlen(s) + 1));
	if (res == 0)
		return (0);
	while (s[i] != '\0')
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
